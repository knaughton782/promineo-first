package com.kirsten.dataTypes;

public class Operations {
	
	public static void main(String[] args) {
		int whatWillWants = 15;
		
		System.out.println("William wants " + whatWillWants + " computers.");
		
		whatWillWants = whatWillWants + 2;
		
		System.out.println("William wants " + whatWillWants + " computers.");
		
		whatWillWants += 2;
		
		System.out.println("William wants " + whatWillWants + " computers.");

		//modulus % - to find the remainder. Often used for odd/even.
		
		
	}
}
